package za.soupcookie.kotlin

fun main(){
    /*
    All the basics: + - * / %
    */

    println(1 + 5)
    println(1 - 5)
    println(2 * 5)
    println(50 / 5)
    println(50 % 6)

    var x = 10
    x += 10 // same math operators as in any language
    x -= 5
    x *= 20
    x /= 15
    x %= 6
    x++ // has simple add/subtracting
    x--

    println(x)
}